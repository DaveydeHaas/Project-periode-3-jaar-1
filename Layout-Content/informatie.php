            <p>De meest kenmerkende verschijnselen zijn wanen, hallucinaties, het wegvallen van de samenhang in spreken en denken, onlogische gedragingen en een gebrek aan emoties en motivatie. Minstens twee van deze symptomen moeten gedurende minimaal een maand optreden.</p>
            <p>De verschijningsvormen kunnen zeer verschillend zijn. Wanneer die verschijnselen in een periode samenkomen, spreken we van een psychose. Steeds terugkerende psychoses verdelen het leven van schizofreniepatiënten vaak in ‘goede en slechte periodes’.</p>
            <p>Omdat de symptomen en verschijnselen zo divers zijn, komt er steeds meer discussie over de term schizofrenie. Tegenstanders hebben het liever over psychosegevoeligheid omdat schizofrenie een stempel is waar mensen moeilijk vanaf komen. Psychosegevoeligheid is dan een meer hoopgevende term. Op dit moment wordt de diagnose schizofrenie echter nog veel gebruikt en veel mensen hebben in het verleden deze diagnose gekregen. Meer informatie over deze discussie is te vinden op www.psychosenet.nl. </p>
            <p>Overigens wordt bij schizofrenie vaak ten onrechte gesproken van een ‘gespleten persoonlijkheid’; dit is echter toe te schrijven aan een andere stoornis (Meervoudige Persoonlijkheid Stoornis, MPS).</p>
